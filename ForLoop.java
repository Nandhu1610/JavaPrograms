package module1.basicjava;

public class ForLoop 
{

	public static void main(String[] args) 
	{
		for(int i=1;i<3;i++)
        {
          System.out.println("Nandhini");
        }
		
		for(int i=1;i<=3;i++)
        {
			System.out.println("Ram");
        }
		
		for(int i=-1;i<=3;i++)
        {
			System.out.println("Sowbarani");
        }
		
		for(int i=0;i<=9;i++)
		{
			System.out.println("Linga");
	    }
		
		for(int i=1;i<=50;i++)
		{
			System.out.println(i);
		}
		
		for(int i=1;i<=5;i--)
		{
			System.out.println(i);
		}
		
		for(int i=1;i>0;i++)
		{
			System.out.println(i);
	    }
		
		for(int i=1;i==10;i--)
		{
			System.out.println(i);
	    }
		
		for(int i=1;i==10;i--)
        {
			System.out.println(i);
        }
		
		for(int i=-10;i<0;i--)
        {
			System.out.println(i);
        }

	}

}
