package module1.basicjava;

public class Datatypes {

	public static void main(String[] args) 
	{
		byte a=10; //declaration and initialization
		System.out.println(a); //utilization
		
		short b=857; //declaration and initialization
		System.out.println(b); //utilization
		
		int c=4448746; //declaration and initialization
		System.out.println(c); //utilization
		
		long d=7655698646l; //declaration and initialization
		System.out.println(d); //utilization
		
		float e=85.568f; //declaration and initialization
		System.out.println(e); //utilization
		
		double pi=3.14598986455; //declaration and initialization
		System.out.println(pi); //utilization
		
		char name='N'; //declaration and initialization
		System.out.println(name); //utilization
		
		String collegename="KSR Institute for Engineering & Technology"; //declaration and initialization
		System.out.println(collegename); //utilization
		
		boolean b1=true; //declaration and initialization
		System.out.println(b1); //utilization
		
		boolean b2=false; //declaration and initialization
		System.out.println(b2); //utilization
		
	}

}
